package model;

public class PerhitunganSementara {

    public Integer jumlah;
    public Integer total;

    public PerhitunganSementara(int jumlah, int total) {
        this.jumlah = jumlah;
        this.total = total;
    }

    public Integer getJumlah() {
        return jumlah;
    }

    public Integer getTotal() {
        return total;
    }

}
